from typing import Dict, Tuple

# Системные промпты для разных сценариев
SYSTEM_PROMPTS = {
    "business_letter": (
        "Ты профессиональный помощник по написанию деловых писем. "
        "Твоя задача - создавать формальные, вежливые и структурированные "
        "деловые письма на русском языке. Используй официально-деловой стиль, "
        "четкие формулировки и правильную деловую этику. "
        "Письмо должно быть логичным, аргументированным и профессиональным."
    ),
    "social_post": (
        "Ты креативный копирайтер для социальных сетей. "
        "Твоя задача - создавать вовлекающие, эмоциональные и запоминающиеся "
        "посты для соцсетей на русском языке. Используй разговорный стиль, "
        "эмодзи, вопросы к аудитории и призывы к действию. "
        "Пост должен быть живым, интересным и побуждать к взаимодействию."
    ),
    "creative": (
        "Ты генератор креативных идей и вдохновляющий мыслитель. "
        "Твоя задача - генерировать оригинальные, нестандартные и творческие "
        "идеи на русском языке. Мысли свободно, предлагай смелые решения, "
        "комбинируй несочетаемое. Твои идеи должны вдохновлять и удивлять."
    )
}


def build_prompt(text: str, scenario: str) -> Dict[str, str]:
    """
    Формирует системный и пользовательский промпты для заданного сценария
    
    Args:
        text: Текст для обработки
        scenario: Сценарий (business_letter, social_post, creative)
    
    Returns:
        Dict с системным и пользовательским промптами
    """
    if scenario not in SYSTEM_PROMPTS:
        raise ValueError(f"Unknown scenario: {scenario}")
    
    system_prompt = SYSTEM_PROMPTS[scenario]
    
    # Пользовательский промпт в зависимости от сценария
    user_prompts = {
        "business_letter": f"Напиши деловое письмо на следующую тему:\n\n{text}",
        "social_post": f"Создай вовлекающий пост для социальных сетей на тему:\n\n{text}",
        "creative": f"Сгенерируй креативные идеи на тему:\n\n{text}"
    }
    
    user_prompt = user_prompts[scenario]
    
    return {
        "system": system_prompt,
        "user": user_prompt
    }